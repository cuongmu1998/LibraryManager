    private void jButton_Add_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Add_ActionPerformed
        // add new Use

        List<LibraryCard> listLib = new LibraryCardDAOImpl().getCardList();
        List<Book> listBook = new BookDAOImpl().getListBook();
        Date brD = txt_BorrowDate.getDate();
        Date rtD = txt_ReturnDate.getDate();
        LibraryCard lib = listLib.get(combo_CardCode.getSelectedIndex());
        int cardId = lib.getId();
        Book b = listBook.get(combo_Book.getSelectedIndex());
        int bookId = b.getBookId();
        // check textfield empty or not
        if (brD == null) {
            JOptionPane.showMessageDialog(null, "This field cannot be empty", "Borrow Date", 2);
        } else if (rtD == null) {
            JOptionPane.showMessageDialog(null, "This field cannot be empty", "Return Date", 2);
        } else {
            if (brD.compareTo(rtD)>0) {
                JOptionPane.showMessageDialog(null, "Return Date must be greater than borrow date", "Return Date", 2);
            } else {
                Borrow br = new Borrow();
                br.setCardId(cardId);
                br.setBookId(bookId);
                br.setBorrowDate(brD);
                br.setReturnDate(rtD);
                boolean bl = new BorrowDAOImpl().insertBorrow(br);
                if (bl) {
                    JOptionPane.showMessageDialog(this, "Insert successful");
                    //refresh Jtable
                    populateJTableWithBorrow();

                    //clear text from the textfield
                    txt_Id.setText("");
                    combo_CardCode.setSelectedIndex(-1);
                    combo_Book.setSelectedIndex(-1);
                    txt_BorrowDate.setDate(null);
                    txt_ReturnDate.setDate(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Add not successful", "Add", 2);
                }
            }
        }
    }//GEN-LAST:event_jButton_Add_ActionPerformed

    private void jButton_Edit_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_ActionPerformed
        // Edit card
        List<LibraryCard> listLib = new LibraryCardDAOImpl().getCardList();
        List<Book> listBook = new BookDAOImpl().getListBook();
        String id = txt_Id.getText().trim();
        Date brD = txt_BorrowDate.getDate();
        Date rtD = txt_ReturnDate.getDate();
        LibraryCard lib = listLib.get(combo_CardCode.getSelectedIndex());
        int cardId = lib.getId();
        Book b = listBook.get(combo_Book.getSelectedIndex());
        int bookId = b.getBookId();
        // check textfield empty or not
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "This field cannot be empty", "Id", 2);
        } else if (brD == null) {
            JOptionPane.showMessageDialog(null, "This field cannot be empty", "Borrow Date", 2);
        } else if (rtD == null) {
            JOptionPane.showMessageDialog(null, "This field cannot be empty", "Return Date", 2);
        } else {
            if (brD.compareTo(rtD)>0) {
                JOptionPane.showMessageDialog(null, "Return Date must be greater than borrow date", "Return Date", 2);
            } else {
                //Calculate age from date of birth by localdate            
                Borrow br = new Borrow();
                br.setBorrowId(Integer.parseInt(id));
                br.setBookId(bookId);
                br.setCardId(cardId);
                br.setBorrowDate(brD);
                br.setReturnDate(rtD);
                boolean bl = new BorrowDAOImpl().updateBorrow(br);
                if (bl) {
                    JOptionPane.showMessageDialog(this, "Edit successful");
                    //refresh Jtable
                    populateJTableWithBorrow();

                    //clear text from the textfield
                    txt_Id.setText("");
                    combo_CardCode.setSelectedIndex(-1);
                    combo_Book.setSelectedIndex(-1);
                    txt_BorrowDate.setDate(null);
                    txt_ReturnDate.setDate(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Edit not successful", "Add", 2);
                }
            }
        }
    }//GEN-LAST:event_jButton_Edit_ActionPerformed

