    private void jTable_Borrow_MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Borrow_MouseClicked
        // Display the selected LibraryCard
        int index =  jTable_Borrow_.getSelectedRow();
        
        //get values
        String id = jTable_Borrow_.getValueAt(index, 0).toString();
        String cardCode = jTable_Borrow_.getValueAt(index, 1).toString();
        String bookName = jTable_Borrow_.getValueAt(index, 2).toString();
        String brD = jTable_Borrow_.getValueAt(index, 3).toString();
        String rtD = jTable_Borrow_.getValueAt(index, 4).toString();
        
        //show data in textfield
        SimpleDateFormat sf = new SimpleDateFormat("dd/MM/yyyy");
        txt_Id.setText(id);
        combo_CardCode.setSelectedItem(cardCode);
        combo_Book.setSelectedItem(bookName);
        try {
            txt_BorrowDate.setDate(sf.parse(brD));
            txt_ReturnDate.setDate(sf.parse(rtD));
        } catch (ParseException ex) {
            Logger.getLogger(ManageBorrowForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable_Borrow_MouseClicked

    private void jButton_FindById_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_FindById_ActionPerformed
        // TODO add your handling code here:
        String strId = JOptionPane.showInputDialog("Enter Id!");
        if (strId!=null && strId.length()>0) {
            try {
                int borrowId = Integer.parseInt(strId);
                Borrow br = new BorrowDAOImpl().getBorrowById(borrowId);
                if (br!=null) {
                    txt_Id.setText(br.getBorrowId()+"");
                    combo_CardCode.setSelectedItem(br.getCardCode());
                    combo_Book.setSelectedItem(br.getBookName());
                    txt_BorrowDate.setDate(br.getBorrowDate());
                    txt_ReturnDate.setDate(br.getReturnDate());
                }else{
                    JOptionPane.showMessageDialog(this, "Invalid id "+strId, "Error", 0);
                }
            } catch (HeadlessException | NumberFormatException e) {
                JOptionPane.showConfirmDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton_FindById_ActionPerformed

