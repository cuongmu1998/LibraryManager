package entities;

/**
 *
 * @author hieu0
 */
public class Author {
    private int authorId;
    private String name;

    public Author() {
    }

    public Author(int authorId, String name) {
        this.authorId = authorId;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAuthorId() {
        return authorId;
    }

    public void setAuthorId(int authorId) {
        this.authorId = authorId;
    }
    
}
